package PersonalPractice;

public class Human {
    private double height;
    private  int age;

    private String name;

    public void setAge(int age) {
        this.age = age;
    }



    public void setHeight(double height) {
        this.height = height;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getHeight() {
        return height;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
     }



