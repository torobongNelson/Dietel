package Chapter4;

public class Increment {
    public static void main(String[] args) {
        int b = 6;

        System.out.println( b++);
        System.out.println(b);

        System.out.println();

        System.out.println();
        int bc = 4;

        System.out.println(++bc);
        System.out.println(bc);
    }
}
 